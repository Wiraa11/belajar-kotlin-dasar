fun main() {

    val result1 = 10.0/3.0
    println(result1)

    val result2 = 10 + 90 / 10
    println(result2)

    // Augmented Assignment
    var total: Int = 0
    val barang1 = 100
    total += barang1

    val barang2 = 100
    total -= barang2

    val barang3 = 100
    total += barang3

    println(total)

    //Ternary operation
    total++
    println(total)

    var suhu = -10
    suhu--
    println(suhu)
}